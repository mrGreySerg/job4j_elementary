package ru.job4j.array;

/**
 * class BarleyBreak creates a two-dimensional array.
 * @author Kustrin Sergei.
 * @version 1.
 */
public class BarleyBreak {

    /**
     * method main.
     * @param args - args.
     */
    public static void main(String[] args) {
        int[][] myArray = new int[3][3];
        myArray[0][0] = 1;
        myArray[0][1] = 2;
        myArray[0][2] = 3;
        myArray[1][0] = 4;
        myArray[1][1] = 5;
        myArray[1][2] = 6;
        myArray[2][0] = 7;
        myArray[2][1] = 8;
        myArray[2][2] = 9;
    }
}
